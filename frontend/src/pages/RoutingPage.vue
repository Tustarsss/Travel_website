              @click="resetRouteForm"
            >
              üîÑ ÈáçÁΩÆÂèÇÊï∞
            </button>
          </div>
        </div>
      </form>
    </PageSection>

    <!-- Âú∞ÂõæÂ±ïÁ§∫ -->
    <PageSection
      title="Âú∞ÂõæÊÄªËßà"
      description="ÁîüÊàêÁöÑË∑ØÁ∫øÂ∞ÜÂú®Âú∞Âõæ‰∏äÂ±ïÁ§∫ÔºåÂåÖÊã¨ÈÄîÁªèËäÇÁÇπÂíåË∑ØÊÆµ‰ø°ÊÅØ„ÄÇ"
    >
      <div class="space-y-4">
        <ErrorAlert v-if="mapError" :message="mapError" />
