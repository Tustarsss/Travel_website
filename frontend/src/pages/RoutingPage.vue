            <div class="space-y-2 md:col-span-2">
              <label class="text-sm font-semibold text-slate-700">
                <span class="flex items-center gap-2">üèõÔ∏è ÁõÆÊ†áÂå∫Âüü</span>
              </label>
              <KeywordSearchSelect
