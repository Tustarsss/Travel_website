                <option v-for="option in routeStrategyOptions" :key="option.value" :value="option.value">
                  {{ option.label }}
                </option>
              </select>
            </div>

            <!-- ‰∫§Êç¢Ëµ∑ÁªàÁÇπÊåâÈíÆ -->
            <div class="flex items-end">
