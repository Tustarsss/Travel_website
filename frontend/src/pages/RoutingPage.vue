              :disabled="routeLoading || !routeForm.regionId || !routeForm.startNodeId || !routeForm.endNodeId"
            >
              {{ routeLoading ? 'üîÑ ËßÑÂàí‰∏≠‚Ä¶' : 'üó∫Ô∏è ËÆ°ÁÆóË∑ØÁ∫ø' }}
            </button>
            <button
