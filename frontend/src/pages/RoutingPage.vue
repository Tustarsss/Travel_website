                :search="searchRegionOptions"
                placeholder="ËæìÂÖ•Âå∫ÂüüÂêçÁß∞ÊàñÂÖ≥ÈîÆËØçÊêúÁ¥¢"
                @select="handleRegionSelect"
                @clear="handleRegionClear"
              />
            </div>

            <!-- Ëµ∑ÁÇπÁªàÁÇπ -->
            <div class="space-y-2">
              <label class="text-sm font-semibold text-slate-700">
                <span class="flex items-center gap-2">üìç Ëµ∑ÁÇπËäÇÁÇπ</span>
              </label>
              <KeywordSearchSelect
                v-model="selectedStartNode"
                :search="searchStartNodeOptions"
                placeholder="ËæìÂÖ•Ëµ∑ÁÇπËäÇÁÇπÂêçÁß∞"
                :disabled="!routeForm.regionId"
                @select="handleStartNodeSelect"
                @clear="handleStartNodeClear"
              />
            </div>

            <div class="space-y-2">
              <label class="text-sm font-semibold text-slate-700">
                <span class="flex items-center gap-2">üéØ ÁªàÁÇπËäÇÁÇπ</span>
              </label>
              <KeywordSearchSelect
                v-model="selectedEndNode"
                :search="searchEndNodeOptions"
                placeholder="ËæìÂÖ•ÁªàÁÇπËäÇÁÇπÂêçÁß∞"
                :disabled="!routeForm.regionId"
                @select="handleEndNodeSelect"
                @clear="handleEndNodeClear"
              />
            </div>

            <!-- Á≠ñÁï•ÈÄâÊã© -->
            <div class="space-y-2">
              <label class="text-sm font-semibold text-slate-700">
                <span class="flex items-center gap-2">üéØ ‰ºòÂåñÁ≠ñÁï•</span>
