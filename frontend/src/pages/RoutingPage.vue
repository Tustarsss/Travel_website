      description="ËøõÂÖ•ÊôØÂå∫ÊàñÂ≠¶Ê†°ÂêéÔºåËæìÂÖ•Ëµ∑ÁÇπÂíåÁªàÁÇπÔºåÁ≥ªÁªü‰ºö‰∏∫ÊÇ®ËßÑÂàíÊúÄ‰ºòÊóÖÊ∏∏Á∫øË∑Ø„ÄÇÊîØÊåÅÊúÄÁü≠Ë∑ùÁ¶ª„ÄÅÊúÄÁü≠Êó∂Èó¥Á≠âÂ§öÁßçÁ≠ñÁï•ÔºåÂèØÈÄâÊã©‰∏çÂêå‰∫§ÈÄöÂ∑•ÂÖ∑„ÄÇ"
    >
      <form class="rounded-2xl border border-slate-200 bg-white p-6 shadow-lg" @submit.prevent="submitRoute">
        <div class="space-y-5">
          <!-- Âå∫ÂüüÈÄâÊã© -->
